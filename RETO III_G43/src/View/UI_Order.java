    private void btn_order_payActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_order_payActionPerformed

        int total_value_pay = 0;
        int quantity_products = 0;
        String name_client = txt_name_c_pay.getText();
        String number_card = txt_card_pay.getText();

        for (SimpleorderClass order_pay : simple_list) {
            if (name_client.equals(order_pay.getClient_name())) {
//              
                total_value_pay = total_value_pay + order_pay.getTotal_value();

                String client_name = order_pay.getClient_name();
                String product_order = order_pay.getProduct_ordered();
                int quantity = order_pay.getQuantity_ordered();
                int price = order_pay.getPrice();
                int total_value = order_pay.getTotal_value();

                CompoundClass compound_order = new CompoundClass(client_name, product_order, quantity, price, total_value);
                compound_list.add(compound_order);

                add_order_pay_list();

                quantity_products = quantity_products + quantity;
            }
        }

        OrderStage stage = new OrderStage(name_client, number_card, total_value_pay);
        orderstage_list.add(stage);

        String mesagge_pay = " El valor total a pagar es:  $ " + total_value_pay;
        txt_area_total_pay.setText(mesagge_pay);

        if (quantity_products <= 20) {
            JOptionPane.showMessageDialog(this, "Orden simple: su orden contiene menos de 20 unidades. Por favor continue con el pago");
        } else {
            JOptionPane.showMessageDialog(this, "Orden compuesta: su orden contiene mas de 20 unidades. Por favor continue con el pago");
        }
    }//GEN-LAST:event_btn_order_payActionPerformed

    private void btn_pay_nowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pay_nowActionPerformed

        String name_client = txt_name_c_pay.getText();
        String card_number = txt_card_pay.getText();
        int total_value = 0;

        for (OrderStage stage : orderstage_list) {
            if (stage.getClient_name().equals(name_client)) {
                int value_pay = stage.getTotal_value();
                total_value = value_pay;
            }
        }

        for (CardClass card_information : card_list) {
            while (card_information.getName_client().equals(name_client)) {

                if (total_value <= card_information.getAmount_card()) {

                    int pay = card_information.getAmount_card() - total_value;
                    card_information.setAmount_card(pay);

                    JOptionPane.showMessageDialog(this, "La orden se ha pagado con exito");
                    clean_fields_pay();

                    for (CompoundClass compound : compound_list) {
                        compound_list.remove();
                    }

                    OrderStage distribution = new OrderStage(name_client, card_number, total_value);
                    distribution_list.add(distribution);
                    add_distribution_list();

                    break;

                } else {
                    JOptionPane.showMessageDialog(this, "Valor insuficiente, intente con otra cuenta");
                    clean_fields_pay();
                    for (CompoundClass compound : compound_list) {
                        compound_list.remove();
                    }
                    break;
                }

            }
        }


    }//GEN-LAST:event_btn_pay_nowActionPerformed

