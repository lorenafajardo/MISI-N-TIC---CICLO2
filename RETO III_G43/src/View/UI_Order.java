    private void btn_add_orderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_orderActionPerformed
        String name_c = txt_name_client_order.getText();
        String address_c = txt_address_client.getText();
        String phone_c = txt_phone_client.getText();
        String email_c = txt_email_client.getText();

        String product_order = txt_product_order.getText();
        int quantity_products_order = Integer.parseInt(txt_quantity_order.getText());

        OrderClass order = new OrderClass(name_c, product_order, quantity_products_order);

        for (StocktakingClass stocktaking : stocktaking_list) {
            if (stocktaking.getName_product().equals(product_order)) {
                if (quantity_products_order <= stocktaking.getQuantity_product()) {

                    order_list.add(order);
                    clean_fields_order();

                    int new_quantity_product = stocktaking.getQuantity_product() - quantity_products_order;
                    stocktaking.setQuantity_product(new_quantity_product);
                    this.add_stock_list();

                    int price_product = stocktaking.getPrice_product();
                    int value_pay = quantity_products_order * stocktaking.getPrice_product();

                    SimpleorderClass order_pay = new SimpleorderClass(name_c, product_order, quantity_products_order, price_product, value_pay);
                    simple_list.add(order_pay);
                    add_order_pay();

                } else {
                    JOptionPane.showMessageDialog(this, "No hay suficientes productos para cubrir su orden");
                }
            }
        }
    }//GEN-LAST:event_btn_add_orderActionPerformed

